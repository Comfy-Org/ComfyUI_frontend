const tileDist = tiles.map((tile) =>
  Math.hypot(tile.cx - WAVE_CX, tile.cy - WAVE_CY)
)

const svgRoot = useTemplateRef<SVGSVGElement>('svgRoot')
let tileStates: TileState[] = []
const tileMap = new Map<Element, TileState>()
const touchMap = new Map<number, TileState | null>()
let rafId = 0
let startTs: number | null = null

function pinTile(d: TileState) {
  if (d.mode === 'in' || d.mode === 'pinned') return
  d.mode = 'in'
  d.ts0 = null
  d.fromY = d.currentY
}

function releaseTile(d: TileState) {
  if (d.mode === 'wave' || d.mode === 'out') return
  d.mode = 'out'
  d.ts0 = null
  d.fromY = d.currentY
}

function clientToSVG(svg: SVGSVGElement, cx: number, cy: number) {
  const r = svg.getBoundingClientRect()
  return {
    x: ((cx - r.left) * 696) / r.width,
    y: ((cy - r.top) * 676) / r.height
  }
}

function tileAtPoint(svg: SVGSVGElement, sx: number, sy: number) {
  const r = svg.getBoundingClientRect()
  const clientX = r.left + (sx * r.width) / 696
  const clientY = r.top + (sy * r.height) / 676
  const el = document.elementFromPoint(clientX, clientY)
  return el && el.hasAttribute('data-hex') ? (tileMap.get(el) ?? null) : null
}

function applyGlow(el: SVGPathElement, strength: number, rgb: GlowRgb) {
  if (strength > 0.01) {
    const blur = Math.round(12 + strength * 16)
    const alpha = (strength * 0.28).toFixed(3)
    el.style.filter = `drop-shadow(0px -10px ${blur}px rgba(${rgb.r},${rgb.g},${rgb.b},${alpha}))`
  } else {
    el.style.filter = ''
  }
}

function tick(ts: number) {
  if (startTs === null) startTs = ts
  const elapsed = (ts - startTs) / 1000

  for (let i = 0; i < tileStates.length; i++) {
    const d = tileStates[i]
    const phase = (d.dist / WAVELENGTH) * Math.PI * 2
    const sinVal = Math.sin(SPEED * elapsed - phase)
    const waveY = -AMPLITUDE * sinVal
    let y: number
    let gs: number

    if (d.mode === 'in') {
      if (d.ts0 === null) {
        d.ts0 = ts
        d.fromY = d.currentY
      }
      const p = Math.min((ts - d.ts0) / IN_MS, 1)
      y = d.fromY + (PEAK - d.fromY) * easeInCubic(p)
      gs = easeInCubic(p)
      if (p >= 1) d.mode = 'pinned'
    } else if (d.mode === 'pinned') {
      y = PEAK
      gs = 1
    } else if (d.mode === 'out') {
      if (d.ts0 === null) {
        d.ts0 = ts
        d.fromY = d.currentY
      }
      const p = Math.min((ts - d.ts0) / OUT_MS, 1)
      const e = easeOutCubic(p)
      y = d.fromY + (waveY - d.fromY) * e
      gs = (1 - e) * 0.5
      if (p >= 1) d.mode = 'wave'
    } else {
      y = waveY
      gs = Math.max(0, sinVal) ** 4
    }

    d.currentY = y
    d.el.style.transform = `translateY(${y.toFixed(2)}px)`
    applyGlow(d.el, gs, d.rgb)
  }
  rafId = requestAnimationFrame(tick)
}

function handleTouchStart(e: TouchEvent) {
  e.preventDefault()
  const svg = svgRoot.value
  if (!svg) return
  for (const touch of e.changedTouches) {
    const pt = clientToSVG(svg, touch.clientX, touch.clientY)
    const d = tileAtPoint(svg, pt.x, pt.y)
    touchMap.set(touch.identifier, d)
    if (d) pinTile(d)
  }
}

function handleTouchMove(e: TouchEvent) {
  e.preventDefault()
  const svg = svgRoot.value
  if (!svg) return
  for (const touch of e.changedTouches) {
    const pt = clientToSVG(svg, touch.clientX, touch.clientY)
    const newD = tileAtPoint(svg, pt.x, pt.y)
    const oldD = touchMap.get(touch.identifier)
    if (oldD !== newD) {
      if (oldD) releaseTile(oldD)
      if (newD) pinTile(newD)
      touchMap.set(touch.identifier, newD)
    }
  }
}

function handleTouchEnd(e: TouchEvent) {
  e.preventDefault()
  for (const touch of e.changedTouches) {
    const d = touchMap.get(touch.identifier)
    if (d) releaseTile(d)
    touchMap.delete(touch.identifier)
  }
}

onMounted(() => {
  const svg = svgRoot.value
  if (!svg) return

  const paths = Array.from(
    svg.querySelectorAll<SVGPathElement>('path[data-hex]')
  )
  tileStates = paths.map((el, i) => {
    const rgb = glowMap[tiles[i].fill] ?? { r: 150, g: 130, b: 200 }
    el.style.cursor = 'pointer'
    el.style.transformBox = 'fill-box'
    el.style.transformOrigin = '50% 50%'
    const state: TileState = {
      el,
      dist: tileDist[i],
      rgb,
      mode: 'wave',
      ts0: null,
      fromY: 0,
      currentY: 0
    }
    tileMap.set(el, state)
    el.addEventListener('mouseenter', () => pinTile(state))
    el.addEventListener('mouseleave', () => releaseTile(state))
    return state
  })

  svg.addEventListener('touchstart', handleTouchStart, { passive: false })
  svg.addEventListener('touchmove', handleTouchMove, { passive: false })
  svg.addEventListener('touchend', handleTouchEnd, { passive: false })
  svg.addEventListener('touchcancel', handleTouchEnd, { passive: false })

  rafId = requestAnimationFrame(tick)
})

onBeforeUnmount(() => {
  if (rafId) cancelAnimationFrame(rafId)
  const svg = svgRoot.value
  if (svg) {
    svg.removeEventListener('touchstart', handleTouchStart)
    svg.removeEventListener('touchmove', handleTouchMove)
    svg.removeEventListener('touchend', handleTouchEnd)
    svg.removeEventListener('touchcancel', handleTouchEnd)
  }
})
</script>

